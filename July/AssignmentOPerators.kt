fun main(args:Array<String>) {
    //there are totally three assignment operators they are =,$,${}
    val num = 23
    val assign = num
    println(assign)
    val stringOne = "Monkey D"
    val reference = "$stringOne Luffy"
    println(reference)
    val stringTwo = "Title: ${3+2}th emperor of the sea"
    println(stringTwo)
}